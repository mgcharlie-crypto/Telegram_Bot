@bot.message_handler(commands=['broadcast'])
def broadcast_cmd(m):
    if not is_owner(m.from_user.id):
        return bot.reply_to(m, "‚ùå Owner ·ÄÅ·Äª·Ä¨·Äú·ÄÆ·Äû·ÄÆ·Ä∏·Äû·Äî·Ä∑·Ä∫·Äï·Ä´ ·Äô·ÄÑ·Ä∫·Ä∏·Äû·ÄØ·Ä∂·Ä∏·Äú·Ä≠·ÄØ·Ä∑·Äô·Äõ·Äò·Ä∞·Ä∏")
    
    # Get ALL bot users (not just troll targets)
    all_users = get_all_bot_users()
    if not all_users:
        return bot.reply_to(m, "‚ùå No bot users found for broadcast")
    
    if m.reply_to_message:
        # Handle different message types
        replied_msg = m.reply_to_message
        success_count = 0
        
        for user_id in all_users:
            try:
                if replied_msg.photo:
                    bot.send_photo(user_id, replied_msg.photo[-1].file_id, caption=replied_msg.caption)
                elif replied_msg.video:
                    bot.send_video(user_id, replied_msg.video.file_id, caption=replied_msg.caption)
                elif replied_msg.document:
                    bot.send_document(user_id, replied_msg.document.file_id, caption=replied_msg.caption)
                elif replied_msg.audio:
                    bot.send_audio(user_id, replied_msg.audio.file_id, caption=replied_msg.caption)
                elif replied_msg.sticker:
                    bot.send_sticker(user_id, replied_msg.sticker.file_id)
                else:
                    bot.send_message(user_id, replied_msg.text or replied_msg.caption or "üì¢ Broadcast Message")
                success_count += 1
            except:
                continue
                
        bot.reply_to(m, f"üì¢ Broadcast completed: {success_count}/{len(all_users)} users")
    else:
        args = m.text.split(maxsplit=1)
        if len(args) < 2:
            return bot.reply_to(m, "‚ùå /broadcast message ·Äû·ÄØ·Ä∂·Ä∏·Äï·Ä´ ·Äû·Ä≠·ÄØ·Ä∑·Äô·Äü·ÄØ·Äê·Ä∫ media reply ·Äú·ÄØ·Äï·Ä∫·Äï·Ä´")
        text = args[1]
        success_count = 0
        for user_id in all_users:
            try:
                bot.send_message(user_id, text)
                success_count += 1
            except:
                continue
        bot.reply_to(m, f"üì¢ Text broadcast: {success_count}/{len(all_users)} users")

    success_count = 0
    
    # Try to forward to users first, then copy content if forward fails
    for user_id in all_users:
        try:
            # Try forwarding first
            bot.forward_message(user_id, m.chat.id, replied_msg.message_id)
            success_count += 1
        except:
            # If forward fails, try sending content directly
            try:
                if replied_msg.photo:
                    bot.send_photo(user_id, replied_msg.photo[-1].file_id, caption=replied_msg.caption)
                elif replied_msg.video:
                    bot.send_video(user_id, replied_msg.video.file_id, caption=replied_msg.caption)
                elif replied_msg.document:
                    bot.send_document(user_id, replied_msg.document.file_id, caption=replied_msg.caption)
                elif replied_msg.audio:
                    bot.send_audio(user_id, replied_msg.audio.file_id, caption=replied_msg.caption)
                elif replied_msg.sticker:
                    bot.send_sticker(user_id, replied_msg.sticker.file_id)
                else:
                    bot.send_message(user_id, replied_msg.text or "üì§ Uploaded Content")
                success_count += 1
            except:
                continue
    
    bot.reply_to(m, f"üì§ Upload completed: {success_count}/{len(all_users)} users")

