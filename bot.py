    if not is_admin(m.from_user.id):
        return bot.reply_to(m, "‚ùå ·Äô·ÄÑ·Ä∫·Ä∏·Äû·ÄØ·Ä∂·Ä∏·ÄÅ·ÄΩ·ÄÑ·Ä∑·Ä∫·Äô·Äõ·Äæ·Ä≠·Äò·Ä∞·Ä∏·Äê·Ä±·Ä¨·Äû·Ä¨·Ä∏")
